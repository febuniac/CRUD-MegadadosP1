
public class Pessoas {

	private Integer id;
	private String nome;
	private String emissor;
	private Integer cpf;
	private Integer rg;
	
	public Integer getId() {return this.id;}
	public void setId(Integer id) {this.id = id;}
	
	public String getNome() {return this.nome;}
	public void setNome(String nome) {this.nome = nome;}
	
	public String getEmissor() {return this.emissor;}
	public void setEmissor(String emissor) {this.emissor = emissor;}
	
	public Integer getCpf() {return this.cpf;}
	public void setCpf(Integer cpf) {this.cpf = cpf;}
	
	public Integer getRg() {return this.rg;}
	public void setRg(Integer rg) {this.rg = rg;}
	
}